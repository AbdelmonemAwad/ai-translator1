def get_translation(key, lang='en', **kwargs):
    """Get translation for a key in specified language"""
    if key not in TRANSLATIONS:
        return key
    
    translation = TRANSLATIONS[key].get(lang, TRANSLATIONS[key].get('en', key))
    
    # Handle string formatting if kwargs provided
    if kwargs:
        try:
            return translation.format(**kwargs)
        except:
            return translation
    
    return translation

def t(key, lang='en', **kwargs):
    """Shorthand for get_translation"""
    return get_translation(key, lang, **kwargs)
